//Task 1:Data type declaration and initialization.
#include<iostream>
using namespace std;
int main()
{
	//Declaring value of all primitive data types.
	int int_var=55;               //INTEGER
	float float_var=3.86;         //FLOAT
	double double_var=55.1234;    //DOUBLE
	char char_var='$';            //CHARACTER
	bool boolean_var=true;        //BOOLEAN
	
	//Printing the value of each variable.
	cout<<"Integer value: "<<int_var<<endl;
    cout<<"Float value: "<<float_var<<endl;
    cout<<"Double value: "<<double_var<<endl;
    cout<<"Character value: "<<char_var<<endl;
    cout<<"Boolean value: "<<boolean_var<<endl;
    return 0;
}
